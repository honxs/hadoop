package cn.mastercom.bigdata.base.model;

/**
 * Domain Object
 * 所有的实体需要继承的接口
 * 仅作标识作用
 * Created by Kwong on 2018/7/13.
 */
public interface DO {

    /**
     * 从格式化的字符串得到成员变量值
     * @param value
     * @throws Exception
     */
    void fromFormatedString(String value) throws Exception;

    /**
     * 将成员变量格式化为字符串，区别于object.toString()
     * @return
     */
    String toFormatedString();
}
